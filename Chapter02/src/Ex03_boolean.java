
public class Ex03_boolean
{

	public static void main(String[] args)
	{
		boolean bCheck1 = true;		//직접 값 대입
		boolean bCheck2 = false;
		boolean bCheck3 = (1 < 2);	//연산의 결과를 값으로 대입
		
		System.out.println(bCheck1);
		System.out.println(bCheck2);
		System.out.println(bCheck3);
		System.out.println(1 > 2);	//연산의 결과를 출력
	}

}
